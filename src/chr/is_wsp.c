#include "lib_chr.h"

int	is_wsp(char c)
{
	if (c == '\t' || c == '\v' || c == '\f' || c == '\r' || c == ' ')
		return (1);
	return (0);
}

int	is_wsp_nl(char c)
{
	if (c == '\t' || c == '\v' || c == '\f' || c == '\r' || c == ' '
		|| c == "\n")
		return (1);
	return (0);
}
